# We can use Operators, to calculate things:
# In Python that is realy simple:

number1 = 50
number2 = 25

added = number1 + number2 # Adding the numbers
subtrahend = number1 - number2 # Subtracting the numbers
multiplied = number1 * number2 # Multiplying the numbers
divided = number1 / number2 # Dividing the numbers

# Printing the result
print(added)
print(subtrahend)
print(multiplied)
print(divided)