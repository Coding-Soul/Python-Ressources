# Datatypes are the different types of variables.
# In python you define those like this:
# Python has only 3 important Datatypes.

name = 'John' # String-Datatype: Strings are used to safe text variables
natural_number = 2 # Integer-Datatypes: Integers are used to safe numbers for mathematical operations. Integers are natural numbers.
float_number = 2.25 # Float-Datatypes: Floats are used to safe decimal numbers. You have to set a . for the Comma.


# Outputs
print(name)
print(natural_number)
print(float_number)